#include "main.h"
/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of adition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - integer divisionof  two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result integer answer
 */
int div(int a, int b)
{
	return ((int)(a / b));
}

/**
 * mod - modulo divide two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of modulo division
 */
int mod(int a, int b)
{
	return (a % b);
}
